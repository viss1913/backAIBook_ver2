/**
 * Генерирует промпт для создания изображения на основе данных книги
 * @param {string} bookTitle - Название книги
 * @param {string} author - Автор книги
 * @param {string} textChunk - Фрагмент текста для иллюстрации
 * @returns {string} Промпт для генерации изображения
 */
export function generateImagePrompt(bookTitle, author, textChunk) {
  return `Ты эксперт по созданию промптов для генерации изображений. 
Создай детальный, художественный промпт на английском языке для генерации иллюстрации к книге.

Книга: "${bookTitle}"
Автор: ${author}

Фрагмент текста для иллюстрации:
${textChunk}

Промпт должен быть:
- Детальным и описательным
- В художественном стиле, соответствующем жанру книги
- Содержать описание атмосферы, настроения и визуальных элементов
- На английском языке
- Готовым для использования в image generation модели
- Без упоминания названия книги или автора в самом промпте

Верни только промпт, без дополнительных объяснений.`;
}

